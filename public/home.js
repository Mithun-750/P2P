  // Function to show previous new release images
  function showPreviousImages() {
    currentIndex4 -= 4;
    if (currentIndex4 < 0) {
      currentIndex4 = Math.floor(new_releases.length / 4) * 4; // Wrap around to the end
    }
    displayImages();
  }



