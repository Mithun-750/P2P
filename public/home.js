// Function to display random images
function displayRandomImages()
 {
  var imageContainer = document.getElementById("image-container");
  var otherImagesContainers =
  document.getElementsByClassName("game-other-images");
  var gamenames = document.getElementsByClassName("game-name");
  var currentIndex1 = Math.floor(Math.random() * games.length);
  var intervalId;

  // Function to get the next image object
  function getNextImage() {
    var game = games[currentIndex1];
    return game;
  }

  // Function to show the main image
  function showMainImage(imageUrl) {
    var mainImageLink = document.createElement("div");
    mainImageLink.classList.add("games");
    // mainImageLink.href = "game-page.html"; // Replace "game-page.html" with the URL of your game page

    var mainImage = new Image();
    mainImage.src = imageUrl;
    mainImage.alt = "Random Image";
    mainImage.style.width = "600px"; // Fixed width
    mainImage.style.height = "350px"; // Fixed height
    mainImage.style.display = "block";
    mainImage.style.objectFit = "fill";
    mainImage.style.objectPosition = "center"; // Center the image

    mainImageLink.appendChild(mainImage);
    imageContainer.innerHTML = ""; // Clear previous content
    imageContainer.appendChild(mainImageLink);
  }

  // Function to handle image hover
  function handleImageHover(imageUrl) {
    return function () {
      showMainImage(imageUrl);
      clearInterval(intervalId); // Pause the interval when hovering over an image
    };
  }

  // Function to handle mouse leave event
  function handleMouseLeave(mainImageUrl) {
    return function () {
      showMainImage(mainImageUrl);
      intervalId = setInterval(showNextImage, 3000); // Resume the interval when the cursor is removed
    };
  }
  // Function to display the next image
  function showNextImage() {
    currentIndex1 = (currentIndex1 + 1) % games.length;
    displayImage(currentIndex1);
  }

  // Function to display the previous image
  function showPreviousImage() {
    currentIndex1 = (currentIndex1 - 1 + games.length) % games.length;
    displayImage(currentIndex1);
  }

  // Function to display the image at a specific index
  function displayImage(index) {
    var imageObject = games[index];

    // Display the game name
    var gameNameContainer = document.querySelector(".game-name");
    var gameNameElement = document.createElement("h3");
    gameNameElement.textContent = imageObject.name;
    gameNameElement.classList.add("game-name");
    gameNameContainer.innerHTML = ""; // Clear previous content
    gameNameContainer.appendChild(gameNameElement);

    // Display the main image
    showMainImage(imageObject.main_image);

    // Display the other images
    for (var i = 0; i < 4; i++) {
      var otherImage = new Image();
      otherImage.src = imageObject.images[i];
      otherImage.alt = "";
      otherImage.style.width = "150px";
      otherImage.style.height = "150px";
      otherImage.style.display = "block";
      otherImage.style.objectFit = "contain";

      // Create anchor tag
      var link = document.createElement("div");
      link.classList.add("games"); // Replace "game-page.html" with the URL of your game page
      link.style.display = "block"; // Set display to block
      link.style.height = otherImage.style.height; // Set height of <a> equal to height of image
      link.appendChild(otherImage);

      // Add event listeners for image hover and mouse leave
      link.addEventListener(
        "mouseenter",
        handleImageHover(imageObject.images[i])
      );
      link.addEventListener(
        "mouseleave",
        handleMouseLeave(imageObject.main_image)
      );

      // Append link to the corresponding container
      otherImagesContainers[i].innerHTML = ""; // Clear previous content
      otherImagesContainers[i].appendChild(link);
    }
  }

  // Add event listener for next button
  document.getElementById("nextButton1").addEventListener("click", function () {
    clearInterval(intervalId);
    showNextImage();
  });

  // Add event listener for previous button
  document.getElementById("prevButton1").addEventListener("click", function () {
    clearInterval(intervalId);
    showPreviousImage();
  });

  // Display the first image
  displayImage(currentIndex1);

  // Start the interval for auto-rotation
  intervalId = setInterval(showNextImage, 8000);
}



function displayOffers() {
  var currentIndex2 = 0; // Variable to keep track of the current index of displayed offer images
  var offerImages = document.querySelectorAll(".offer-games .offer-image");

  function showNextOffers() {
      currentIndex2 = (currentIndex2 + 4) % offer_games.length; // Update the current index
      displayOffers(currentIndex2); // Display the next set of offer images
  }

  function showPrevOffers() {
      currentIndex2 = (currentIndex2 - 4 + offer_games.length) % offer_games.length; // Update the current index
      displayOffers(currentIndex2); // Display the previous set of offer images
  }

  function displayOffers(startIndex) {
      var maxToShow = Math.min(offer_games.length - startIndex, offerImages.length); // Maximum number of images to display
      for (var i = 0; i < maxToShow; i++) {
          var offerIndex = (startIndex + i) % offer_games.length; // Calculate the index of the offer to display

          var offer = offer_games[offerIndex];

          var offerImageDiv = offerImages[i];

          var offerImageLink = document.createElement("div");
          offerImageLink.classList.add("games");
          // offerImageLink.href = "game-page.html"; // Replace "game-page.html" with the URL of your game page

          var offerImage = new Image();
          offerImage.src = offer.image;
          offerImage.alt = "Offer Image";
          offerImage.style.width = "210px"; // Fit to the container width
          offerImage.style.height = "282px"; // Fit to the container height
          offerImage.style.objectFit = "fill"; // Cover the container

          offerImageLink.appendChild(offerImage);
          offerImageDiv.innerHTML = ""; // Clear previous content
          offerImageDiv.appendChild(offerImageLink);

          // Create a div for displaying price and name information
          var infoDiv = document.createElement("div");
          infoDiv.classList.add("info");

          var name = document.createElement("p");
          name.textContent = offer.name;

          var priceDiv = document.createElement("div");
          priceDiv.classList.add("price-info");

          var originalPrice = document.createElement("span");
          originalPrice.textContent = " $" + offer.original_price;

          var offerPrice = document.createElement("span");
          offerPrice.textContent = "$" + offer.offer_price;

          var offerPercentage = document.createElement("span");
          var percentageDifference =
              ((offer.original_price - offer.offer_price) / offer.original_price) * 100;
          offerPercentage.textContent = "-" + percentageDifference.toFixed(0) + "%";

          priceDiv.appendChild(offerPercentage);
          priceDiv.appendChild(originalPrice);
          priceDiv.appendChild(offerPrice);
          infoDiv.appendChild(name);
          infoDiv.appendChild(priceDiv);

          offerImageDiv.appendChild(infoDiv);
      }
  }

  // Add event listener for the forward button
  document.getElementById("nextButton2").addEventListener("click", showNextOffers);

  // Add event listener for the backward button
  document.getElementById("prevButton2").addEventListener("click", showPrevOffers);

  displayOffers(currentIndex2);
}


